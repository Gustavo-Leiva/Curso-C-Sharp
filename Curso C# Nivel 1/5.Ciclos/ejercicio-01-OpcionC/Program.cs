namespace ejercicio_01_OpcionC;
class Program
{
    static void Main(string[] args)
    {
        //1. Hacer un programa para mostrar los números del 1 al 10. 
        //No se debe realizar ningún pedido de datos.

        //resuelto con ciclo Do While

        int i = 0;

        do
        {
            Console.WriteLine(i);
            i++;

        } while (i <= 10);

    }
}
